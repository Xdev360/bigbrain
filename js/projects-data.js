window.BB_getService = function(id){
  var list = window.BB_SERVICES || [];
  for(var i=0;i<list.length;i++){ if(list[i].id===id) return list[i]; }
  return null;
};

window.BB_projectsForService = function(serviceId){
  return (window.BB_PROJECTS||[]).map(function(p){
    var phase = null;
    (p.phases||[]).forEach(function(ph){ if(ph.id===serviceId) phase = ph; });
    if(!phase) return null;
    return { project:p, phase:phase };
  }).filter(Boolean);
};
